// Ensure the background mode is applied on load: prefer stored user choice, else use image
document.addEventListener('DOMContentLoaded', () => {
    try {
        const stored = localStorage.getItem('bgMode');
        if (stored === 'gradient') {
            document.documentElement.classList.add('bg-gradient');
            document.documentElement.classList.remove('bg-image');
        } else {
            document.documentElement.classList.add('bg-image');
            document.documentElement.classList.remove('bg-gradient');
        }
    } catch (e) {
        // If localStorage is unavailable, default to image mode
        document.documentElement.classList.add('bg-image');
    }
});